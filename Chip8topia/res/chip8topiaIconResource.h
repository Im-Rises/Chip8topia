            chip8Icon[i * IMAGE_CHANNELS] = CHIP8TOPIA_ICON_DATA[i];
            chip8Icon[i * IMAGE_CHANNELS + 1] = CHIP8TOPIA_ICON_DATA[i];
            chip8Icon[i * IMAGE_CHANNELS + 2] = CHIP8TOPIA_ICON_DATA[i];
